# 什么是xpath？
# xpath（ xml path language）是一门在XML文档中查找信息的语言，可用来在 XML 文档中对元素和属性进行遍历。
# 可以遍历和搜索XML文档中的文本和属性，同时用于HTML

# 什么是xml
# 是一门可扩展的编译语言，是为了传输数据据，内部的标签可字定义
# xml的结构类似于html

# 安装 xml 库：pip install lxml

# xpath语法的路径表达式（常用)
#
# nodename(节点名称) 获取此节点下的子节点
# /                 获取当前节点下的直接子节点
# //                获取当前节点下的节点，不考虑节点位置
# .                 获取当前节点
# ..                获取当前节点的父节点
# @                 获取属性

